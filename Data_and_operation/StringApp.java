
public class StringApp {

	public static void main(String[] args) {
		System.out.println("Hello World"); // Hello World
		// " " String : character들이 모여서 만든 문자열
		System.out.println('H'); // H
		// ' ' Character : 1개의 문자
		System.out.println("Hello \nWorld"); // Hello	
											 // World
		// \n (new line) : 줄바꿈
		System.out.println("Hello \"World\""); // Hello "World"
		// \" (escape) : \다음의 "을 문자로 인식
	}

}
