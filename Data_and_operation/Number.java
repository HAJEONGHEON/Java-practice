
public class Number {

	public static void main(String[] args) {
		System.out.println(6+2); // 8
		System.out.println(6-2); // 4
		System.out.println(6*2); // 12
		System.out.println(6/2); // 3
		
		System.out.println(Math.PI); // 3.141592653589793
		// Math. : 수학과 관련된 여러 명령어들의 캐비넷 
		System.out.println(Math.floor(Math.PI)); // 3
		// floor() : 내림	
		System.out.println(Math.ceil(Math.PI)); // 4
		// ceil() :  올림
		System.out.println(Math.abs(6*-6)); // 36
		// abs() : 절대값
		System.out.println(Math.pow(3, 2)); // 3^2
		// pow(a,b) : a^b			
		System.out.println(Math.hypot(3, 4)); // 3^2 + 4^2 = 5^2
		// hypot(a,b) : a^2 + b^2
		
	}

}
